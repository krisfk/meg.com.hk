引言

本程式是為協助學員熟習電腦化筆試情況而設計。
正式考試時，電腦化筆試的形式為多項選擇題，當中只有一個正確答案。考試時，考生可採用輕觸式熒幕或滑鼠作答。
模擬考試，每組各有20條題目，而試題內容只作示範及參考之用。在正式考試時，考生須回答試卷內全部之試題，考試時間合共為 20 分鐘。

 
指引

(1)	細閱指引內容後，請按下「開始考試」，程式將會顯示筆試題目。
(2)	模擬筆試合共20條試題，考生須於三個項目中選擇一個最恰當的作為答
案。
(3) 當選擇答案後，請按下「確認本題答案」，系統會自動顯示正確答案。再按
   「下一條題目」以顯示下一條試題。
(4) 如要終止考試，請按「完成並交卷」。
(3) 考生須限時20分鐘內，答對16條或以上之試題方為合格。

模擬筆試只供學員練習之用，想瞭解更多詳情，請致電 +852 2866-6682查詢。
						  

