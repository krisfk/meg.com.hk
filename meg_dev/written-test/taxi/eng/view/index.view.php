引言

本程式是為協助學員熟習的士筆試情況而設計。
正式考試時，的士筆試的形式為多項選擇題，當中只有一個正確答案。考試時，考生可採用輕觸式熒幕或滑鼠作答。
模擬考試，會有『甲、乙、丙』三部份給學員自行選擇。『甲、乙』每組各有20條題目，丙部份則有合共100條題目，而試題內容只作示範及參考之用。在正式考試時，試卷分為三部份，考生須回答試卷內全部之試題，考試時間合共為 70 分鐘。

 
指引

(1) 甲部：
的士則例共有 20 條之試題，考生須於四個項目中選擇一個最恰當的作為答案，及格標準是不可答錯多於 2 條的題目。
(2) 乙部：
地方試題共有 20 條之試題，考生須於四個項目中選擇一個最恰當的作為答案，及格標準是不可答錯多於 2 條的題目。
(3) 丙部：
道路使用者守則共有 100 條之試題，考生須於三個項目中選擇一個最恰當的作為答案，及格標準是不可答錯多於 5 條的題目。

考生需於上述三個部份均達到及格標準，他的總成績才會被視為及格。但如
考生的成績有任何一部份不符合上述及格標準，考試結果便會被列作不及格論。

模擬筆試只供學員練習之用，想瞭解更多詳情，請致電 +852 2866-6682查詢。
