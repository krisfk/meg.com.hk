	function showLoc() {

		var hash = location.hash;

		if (!hash) {
			hash = '#meg-1';
			
		}
		var obj = $('.shop-menu ul li a[href="' + hash + '"]');
		obj.toggleClass('active');
	
		
		var address = obj.attr('data-address');
		
		var map = obj.attr('data-map');

		$('.address').html(address);
		$('.map-iframe iframe').attr('src', map);
		
		var lat = obj.attr('data-lat');
		var lon = obj.attr('data-lon');
		var shop_name = obj.attr('data-name');
		create_map(lat,lon,shop_name,address);
	}

});


function create_map(lat,lon,shop_name,address) {
	
			var myOptions = {
				zoom: 18,
				center: new google.maps.LatLng( lat, lon ),
				mapTypeId: google.maps.MapTypeId.ROADMAP,
				gestureHandling: "greedy" 
			};
			map = new google.maps.Map( document.getElementById( 'gmap_canvas' ), myOptions );
			marker = new google.maps.Marker( {
				map: map,
				position: new google.maps.LatLng( lat, lon )
			} );
	
			map.drawRoute({
				//22.2803747,114.1828534
				//22.279922,114.1820828
			  origin: [22.2803747, 114.1828534],
			  destination: [22.279922, 114.1820828],
			  travelMode: 'driving',
			  strokeColor: '#131540',
			  strokeOpacity: 0.6,
			  strokeWeight: 6
			});

	
	
			infowindow = new google.maps.InfoWindow( {
				content: '<div style="width:250px;"><strong style="margin:0 0 10px 0;display:block;font-size:18px;font-weight:bold;font-family: 微軟正黑體, arial;">'+shop_name+'</strong>'+address+'</div>'
			} );
			google.maps.event.addListener( marker, 'click', function () {
				infowindow.open( map, marker );
			} );
			infowindow.open( map, marker );
		
//		google.maps.event.addDomListener( window, 'load', showLoc );
		
}
